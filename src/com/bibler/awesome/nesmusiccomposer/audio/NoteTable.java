	public static int getNote(int index) {
		return notes[index];
	}
	
	public static int getNoteLength(int index) {
		return noteLengths[index];
	}
	
	public static int getNoteLengthFromWidth(int width) {
		for(int i = 0; i < noteLengths.length; i++) {
			if(i == width) {
				return i;
			}
		}
		return -1;
	}

